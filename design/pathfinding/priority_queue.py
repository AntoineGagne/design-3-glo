""" This module contains a priority heap for the graph """

import heapq


class PriorityQueue():
    """Priorty Queue"""

    def __init__(self):
        self.elements = []

    def empty(self):
        return len(self.elements) == 0

    def put(self, item, priority):
        heapq.heappush(self.elements, (priority, item))

    def get(self):
        return heapq.heappop(self.elements)[1]
